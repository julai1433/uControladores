/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_I2C1_Init(void);
static void MX_USART1_UART_Init(void);

/* Private function prototypes ----------------------------------------------*/

RawData_Def myAccel_Raw, myGyro_Raw;
ScaledData_Def myAccelScaled, myGyroScaled;
char dataOut[100];

int main(void)
{
  /* MCU Configuration----------------------------------------------------------*/
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
	MPU_ConfigTypeDef myMpuConfig;
  /* Configure the system clock */
  SystemClock_Config();
  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();
	//1. Inicializar el módulo MPU6050 y el I2C
	MPU6050_Init(&hi2c1);
	//2. Configurar parámetros del Acelerómetro y Giroscopio
	myMpuConfig.Accel_Full_Scale = AFS_SEL_2g;
	myMpuConfig.ClockSource = Internal_8MHz;
	myMpuConfig.CONFIG_DLPF = DLPF_260A_256G_Hz;
	myMpuConfig.Gyro_Full_Scale = FS_SEL_500;
	myMpuConfig.Sleep_Mode_Bit = 0;		//1: Sleep Mode ON; 0: Sleep Mode OFF
	MPU6050_Config(&myMpuConfig);
	
  for(;;)
  {
		MPU6050_Get_Accel_Scale(&myAccelScaled);
		MPU6050_Get_Gyro_Scale(&myGyroScaled);
		sprintf(dataOut,"Acelerometro: %f,%f,%f\nGiroscopio: %f,%f,%f\n",myAccelScaled.x,myAccelScaled.y,myAccelScaled.z,myGyroScaled.x,myGyroScaled.y,myGyroScaled.z);
		HAL_UART_Transmit(&huart1, dataOut,strlen(dataOut), 100);
  }

}

/**
  * @brief System Clock Configuration
  * @retval None
  */
